#Operadores Aritmeticos, comparadores y logicos

#Operadores Aritmeticos
num=2
x=num
print("Para x=2")
#Adicion
print("Adicion x+2:")
x+=2
print(x)

#Sustraccion
x=num
print("Sustraccion x-1:")
x-=1
print(x)

#Multiplicacion
x=num
print("Multiplicacion x*4:")
x*=4
print(x)

#Divicion
x=num
print("Divicion x/2:")
x/=2
print(x)

#Modulo
x=num
print("Modulo x%4:")
x%=4
print(x)

#Division de piso (Devuelve el entero menor mas cervano a la divicion)
x=num
print("Divicion de piso x//3:")
x//=3
print(x)

#Exponente
x=num
print("Exponente x^5:")
x**=5
print(x)

print("")

#Operadores Comparativos
print("Para a=5 y b=8")
a=5
b=8

#Menor que
print("a es menor que b:")
print(a<b)

#Mayor que
print("a es mayor que b:")
print(a>b)

#Menor o igual que
print("a es menor o igual que b:")
print(a<=b)

#Mayor que
print("a es mayor o igual que b:")
print(a>=b)

#Igual que
print("a es igual que b:")
print(a==b)

#DIferente de
print("a es diferente de b:")
print(a!=b)

